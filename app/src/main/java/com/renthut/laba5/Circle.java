package com.renthut.laba5;

public class Circle {
    private int number;
    private int color;

    public Circle() {
    }

    public Circle(int number, int color) {
        this.number = number;
        this.color = color;
    }

    public int getNumber() {
        return number;
    }

    public void setNumber(int number) {
        this.number = number;
    }

    public int getColor() {
        return color;
    }

    public void setColor(int color) {
        this.color = color;
    }
}
